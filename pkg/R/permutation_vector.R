## helpers
.valid_permutation_vector <- function(x) {
    perm <- get_order(x)
    valid <- TRUE
    
    tab <- table(perm)
    if(any(tab != 1)) valid <- FALSE
    if(length(tab) != length(perm) 
	|| any(names(tab) != sequence(length(perm)))) valid <- FALSE

   

    if(!valid) stop("Invalid permutation vector!\nVector: ", 
	    paste(perm, collapse=", "))
}

